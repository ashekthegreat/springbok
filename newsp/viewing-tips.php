                            <h1>Springbok Top Tips for Viewings</h1>

                            <img class="img-responsive" src="assets/img/viewing-tips.png">

                            <p>&nbsp;</p>

                            <p>Looking around a property that could become your new home is exciting. Here are some essential tips that will help you complete successful viewings: </p>

                            <ul class="list-gap">
                                <li><strong>Fly-by:</strong> before you arrange a viewing, drive or (ideally) walk past the property. You can often eliminate inappropriate properties with this one simple step.</li>
                                <li><strong>Night and day:</strong> try to do multiple fly-bys at different times. This will help you gauge traffic, noise levels, safety, street lighting, parking and a general feel for the local community.</li>
                                <li><strong>Think first:</strong> keep a list of the things that are important for your next home. When you start viewing, the list will be short, but as you see more properties it will grow. There’s nothing wrong with bringing a checklist and politely working through it as you view the property.</li>
                                <li><strong>Speed:</strong> if you have a property in mind, view it as soon as possible. Leave it too long and you could lose out. This is especially true at Springbok, as our properties sell fast!</li>
                                <li><strong>Make the effort:</strong> it’s tempting to be picky about appointment times, but you should try to be as flexible as possible. Make good use of your lunch hour or visit properties on the way to and from work. The more flexible you are, the quicker you’ll see properties and find the ideal new home.</li>
                                <li><strong>Weekend viewings:</strong> we know you’re a busy person, so we can arrange viewings at the weekend. They’re often more relaxed, as everyone is in less of a rush, so take advantage and fill your schedule.</li>
                                <li><strong>Buying with a partner?</strong> If you’re buying with a partner, take them along with you. If you’re buying alone, take an interested friend – it’s always good to have a second opinion and an additional pair of eyes.</li>
                                <li><strong>Daylight:</strong> always try to view properties in daylight. No matter how well-lit a property is, natural light will show you more and allow you to get the best out of your viewing time.</li>
                                <li><strong>Arrive on time:</strong> plan your journey in advance and make sure you arrive on time. Viewings are limited in length and you don’t want to be rushed. Google Maps can help you here.</li>
                                <li><strong>Rapport:</strong> try to create a friendly relationship with the seller. It helps with the whole process, especially with the “nosier” aspects of viewing, and makes negotiations a lot more pleasant.</li>
                                <li><strong>Open mind:</strong> we’re sure you have a mental image of your dream home, but try to keep an open mind when viewing. People frequently end up buying a property that is very different to the home they imagined, but which turns out to be better.</li>
                                <li><strong>Don’t point:</strong> if you happen to notice flaws, try not to point them out to the seller unless you need clarification. Instead, give feedback to the sales negotiator. This will help you maintain your rapport with the seller.</li>
                                <li><strong>Leave on a positive note:</strong> always try to end the appointment on a high note. This goes back to the rapport tip, above – if you are friendly, the seller is more likely to remember you and treat your offer favorably.</li>
                                <li><strong>Quick and honest feedback:</strong> after a viewing, be sure to give us honest feedback as early as possible. This helps us find properties that will suit you, as well as addressing any problems.</li>

                            </ul>
