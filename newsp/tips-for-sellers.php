                            <h3 class="text-green">Tell us what you need</h3>
                            <p>We deal with people in all sorts of circumstances. Some are just testing the water and aren't 100% committed to moving, while others are on specific timelines. Whatever the situation, we need to know so that we can do our utmost to meet your needs. Whether you can’t move for six months or need to find a new home in six days, tell us.</p>

                            <h3 class="text-green">Prices in your local area</h3>
                            <p>Be realistic with your asking price, to generate maximum interest. Our property valuers are a great source of information and advice, and they have your interests at heart. They can provide “comparables” – details of recent listings and sales of similar properties in the area – to help you figure out the right price to ask. If you deal with another agent who gives you a much higher valuation figure, ask them how they worked it out and verify the comparables!</p>

                            <h3 class="text-green">Line up your solicitor and do some pre-planning</h3>
                            <p>It’s always best to have a solicitor lined up right from the start, to avoid delays later on. Springbok’s recommended solicitors are all independents who are efficient and professional. Make sure you have your documents ready, as well – your deeds, details of mortgage lenders, planning permissions and everything else. The more you have to hand, the less time you’ll waste in searching when you’ve got a buyer lined up!</p>

                            <h3 class="text-green">Appearance matters, whatever the price</h3>
                            Appearance is absolutely critical in selling property. First impressions count for a lot and generate a bigger emotional response than anything else, even the price! Prospective buyers have all the technical details when they come to view, but they will react to what they see, hear, feel and smell while they look around. Make sure your property feels as good as possible, even if you’ve priced to sell.

                            <h3 class="text-green">Be flexible</h3>
                            <p>Be as flexible as possible for viewings. If a potential buyer wants a second visit, be at home. Make sure your agent and solicitor have your phone number and can contact you quickly. Pick up your messages as soon as possible if you miss a call. Your availability can affect offers, so make sure you don’t cause delay – be available so that you get the best possible price for your property.</p>

                            <h3 class="text-green">Let there be light</h3>
                            <p>Let the sun shine in! A gloomy, musty house is much harder to sell than one which looks bright and cheery, so open the curtains, let in some fresh air and show prospects what a lovely home you have. The more light you can let in, the better.</p>

                            <h3 class="text-green">Avoid crowd scenes</h3>
                            <p>Potential buyers can feel like intruders if they visit a home that’s filled with people. If you have a large family, try to get them out of the house when buyers come to view, or your visitors are likely to hurry through and not give the property the attention it deserves. Keep the number of inhabitants to a minimum and let your buyers view in peace.</p>

                            <h3 class="text-green">Relax</h3>
                            <p>The ideal guide is one who is friendly, but who doesn't try to force conversation. Prospective buyers want to view your home with a minimum of distraction, so introduce them to each room, then wait patiently and quietly for them to look around. They will let you know when they want to move on. The trick is to be present but not invasive, available for questions without offering a running commentary.</p>

                            <h3 class="text-green">Defer to experience</h3>
                            <p>If your potential buyer wants to talk about price, terms, dates or any other property matters, ask them to speak to your Springbok Sales Negotiator. You are there to show them around, not to discuss matters that are best left to us. Some buyers will try to push you into a corner by negotiating in person, so send them to us and let us handle it.</p>

                            <h3 class="text-green">It’s better with more prospects</h3>
                            <p>When you maximise your home’s marketability, you are likely to attract multiple buyers. This is a much better situation than only having a single prospect, even one who is very interested in the property. When you have several prospective buyers, they will compete with each other; a single buyer only competes with you.</p>

                            <h3 class="text-green">Put emotion on the back-burner</h3>
                            <p>One of the hardest things to do is to step back from all the emotion that’s invested in your home. It’s very important to be detached and approach negotiations logically and practically. Your Springbok sales negotiator knows this and will help you consider each offer sensibly, giving you an advantage over people who get emotionally caught up in the situation.</p>

